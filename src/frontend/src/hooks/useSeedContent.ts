export function useSeedContent() {
  const { isAdmin } = useIsAdmin();
  const { data: publishedContent, isLoading } = usePublishedContent();
  const { actor } = useActor();
  const seededRef = useRef(false);

  useEffect(() => {
    if (!isAdmin || isLoading || !actor || seededRef.current) return;

    const alreadyExists = publishedContent?.some(
      (item) => item.title === VOYHOTTA_TITLE,
    );

    if (!alreadyExists) {
      seededRef.current = true;
      (async () => {
        try {
          const id = await actor.createDraft(VOYHOTTA_TITLE, VOYHOTTA_CONTENT);
          await actor.publishContent(id);
        } catch (e) {
          seededRef.current = false;
          console.error("Failed to seed ভয়হত্যা:", e);
        }
      })();
    } else {
      seededRef.current = true;
    }
  }, [isAdmin, isLoading, publishedContent, actor]);
}
